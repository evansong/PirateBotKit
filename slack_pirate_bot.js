if (!process.env.token) {
    console.log('Error: Specify token in environment');
    process.exit(1);
}

var Botkit = require('./lib/Botkit.js');
var os = require('os');

var controller = Botkit.slackbot({
    debug: true,
});

var bot = controller.spawn({
    token: process.env.token
}).startRTM();

function logBody(error, response, body) {
    if (!error && response.statusCode == 204) {
        console.log(body)
    }
}


var azureWebApiUrl = "http://pirateapi20160517022351.azurewebsites.net/api/pirates";

// allows the user to add pirate key value pairs
function processPostMessage(bot, message) {
    var postValue = escape(message.text.substring(10));
    var urlRequest = azureWebApiUrl + '?' + postValue;
    var request = require('request');

    request.post(urlRequest, logBody(error, response, body)); 
}

// handle posts to WebAPI
controller.hears(['postpirate'], 'direct_message,direct_mention,mention', processPostMessage(bot, message));

function getResponseHandler(error, response, body) {
    if (!error && response.statusCode == 200) {
        bot.reply(message, body.trim());
    }
}

// gets corresponding word / phrase for the key
function processGetMessage(bot, message) {
    var urlRequest = azureWebApiUrl + '?phrase=' + escape(message.text.substring(6));
	var request = require('request');

	request(urlRequest, getResponseHandler); 
}

// handle get requests
controller.hears(['pirate'], 'direct_message,direct_mention,mention', processGetMessage(bot, message));


controller.hears(['call me (.*)', 'my name is (.*)'], 'direct_message,direct_mention,mention', function(bot, message) {
    var name = message.match[1];
    controller.storage.users.get(message.user, function(err, user) {
        if (!user) {
            user = {
                id: message.user,
            };
        }
        user.name = name;
        controller.storage.users.save(user, function(err, id) {
            bot.reply(message, 'Got it. I will call you ' + user.name + ' from now on.');
        });
    });
});

controller.hears(['shutdown'], 'direct_message,direct_mention,mention', function(bot, message) {

    bot.startConversation(message, function(err, convo) {

        convo.ask('Are you sure you want me to shutdown?', [
            {
                pattern: bot.utterances.yes,
                callback: function(response, convo) {
                    convo.say('Bye!');
                    convo.next();
                    setTimeout(function() {
                        process.exit();
                    }, 3000);
                }
            },
        {
            pattern: bot.utterances.no,
            default: true,
            callback: function(response, convo) {
                convo.say('*Phew!*');
                convo.next();
            }
        }
        ]);
    });
});
